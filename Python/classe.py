#Criando um aclasse calculadora!
class calculador:
    #Usando o metodo init
    def __init__(self):
        pass

    def soma(self , a, b):
        return a+b

    def sub(self, a, b):
        return a-b

    def mult(self, a, b):
        return a*b

    def div(self, a, b):
        return a/b

calculador = calculador()
